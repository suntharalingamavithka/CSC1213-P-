import java.util.Scanner;
public class Ex01{
	public static void main(String[] args)
	{
		Scanner ob=new Scanner(System.in);
		int number[]=new int[5];
		int sum=0;
		for(int i=0;i<5;i++)
		{
			System.out.print("Enter the number "+(i+1)+" :");
			number[i]=ob.nextInt();
			sum=sum + number[i];
		}
		System.out.print("The sum is:"+sum);
	}
}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		